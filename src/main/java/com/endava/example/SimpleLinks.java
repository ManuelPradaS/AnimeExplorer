package com.endava.example;

public class SimpleLinks {

    private String self;

    public String getSelf() {
        return self;
    }

    public void setSelf(String self) {
        this.self = self;
    }


    @Override
    public String toString() {
        return "SimpleLinks{" +
                "self='" + self + '\'' +
                '}';
    }
}
